import fs from 'node:fs/promises'
import path from 'node:path'

const repoRoot = process.cwd()
const pagesDir = path.join(repoRoot, 'src/js/pages')
const reportPath = path.join(repoRoot, 'openspec/changes/security-implementation-plan/xss-audit-checklist.md')
const safePatterns = [
  /\bescAttr\s*\(/,
  /\bescapeHTML\s*\(/,
  /\bsanitizeHTML\s*\(/,
  /\bsanitizeSVG\s*\(/,
  /\bformatCurrency\s*\(/,
  /\bformatDate\s*\(/,
  /\bMath\./,
  /^['"`].*['"`]$/s,
  /^\d+(\.\d+)?$/,
  /^(true|false|null|undefined)$/,
  /^[a-zA-Z_$][\w$]*Html$/,
  /^[a-zA-Z_$][\w$]*Options$/,
  /^[a-zA-Z_$][\w$]*Style$/,
  /^this\.getAvailableColors\(\)\.map\(color\s*=>/s,
  /^colors\.map\(c\s*=>/s,
  /^u\.role === 'owner' \?/s,
]
const suspiciousTokens = /(name|description|message|title|email|author|icon|color|url|svg|preview|server|displayName|emailAddress)/i

function isExpressionSafe(expression) {
  const normalized = expression.trim().replace(/\s+/g, ' ')
  if (!normalized) return true
  return safePatterns.some(pattern => pattern.test(normalized))
}

function isExpressionRelevant(expression) {
  const normalized = expression.trim().replace(/\s+/g, ' ')
  if (!normalized) return false
  if (suspiciousTokens.test(normalized)) return true
  if (/\?\s*['"`]/.test(normalized) && /:\s*['"`]/.test(normalized)) return false
  return false
}

function findMatchingBacktick(source, startIndex) {
  let index = startIndex + 1
  let escaped = false

  while (index < source.length) {
    const char = source[index]
    if (!escaped && char === '`') {
      return index
    }
    escaped = !escaped && char === '\\'
    if (char !== '\\') {
      escaped = false
    }
    index += 1
  }

  return -1
}

function extractExpressions(templateLiteral) {
  const expressions = []

  for (let index = 0; index < templateLiteral.length; index += 1) {
    if (templateLiteral[index] !== '$' || templateLiteral[index + 1] !== '{') continue

    let depth = 1
    let cursor = index + 2
    while (cursor < templateLiteral.length && depth > 0) {
      if (templateLiteral[cursor] === '{') depth += 1
      if (templateLiteral[cursor] === '}') depth -= 1
      cursor += 1
    }

    expressions.push({
      expression: templateLiteral.slice(index + 2, cursor - 1),
      offset: index,
    })
    index = cursor - 1
  }

  return expressions
}

function lineNumberAt(source, offset) {
  return source.slice(0, offset).split('\n').length
}

async function getPageFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true })
  return entries
    .filter(entry => entry.isFile() && entry.name.endsWith('.js'))
    .map(entry => path.join(dir, entry.name))
}

async function main() {
  const files = await getPageFiles(pagesDir)
  const findings = []

  for (const filePath of files) {
    const source = await fs.readFile(filePath, 'utf8')
    let searchIndex = 0

    while (searchIndex < source.length) {
      const innerHtmlIndex = source.indexOf('innerHTML', searchIndex)
      if (innerHtmlIndex === -1) break

      const equalsIndex = source.indexOf('=', innerHtmlIndex)
      const templateStart = source.indexOf('`', equalsIndex)
      if (equalsIndex === -1 || templateStart === -1) {
        searchIndex = innerHtmlIndex + 'innerHTML'.length
        continue
      }

      const between = source.slice(equalsIndex + 1, templateStart)
      if (!/^\s*$/.test(between)) {
        searchIndex = innerHtmlIndex + 'innerHTML'.length
        continue
      }

      const templateEnd = findMatchingBacktick(source, templateStart)
      if (templateEnd === -1) break

      const templateLiteral = source.slice(templateStart + 1, templateEnd)
      const expressions = extractExpressions(templateLiteral)
      for (const { expression, offset } of expressions) {
        if (isExpressionSafe(expression) || !isExpressionRelevant(expression)) continue
        findings.push({
          filePath,
          line: lineNumberAt(source, templateStart + offset),
          expression: expression.trim(),
        })
      }

      searchIndex = templateEnd + 1
    }
  }

  const relativeReportPath = path.relative(repoRoot, reportPath)
  const reportLines = [
    '# XSS Sink Audit Checklist',
    '',
    `Generated by \`scripts/audit-xss-sinks.mjs\`.`,
    '',
  ]

  if (findings.length === 0) {
    reportLines.push('No unsanitized template-driven `innerHTML` expressions found under `src/js/pages`.')
  } else {
    reportLines.push('The following interpolations should be reviewed and wrapped with `escAttr()` or another context-appropriate sanitizer:')
    reportLines.push('')
    for (const finding of findings) {
      reportLines.push(`- [ ] ${path.relative(repoRoot, finding.filePath)}:${finding.line} -> ${finding.expression}`)
    }
  }

  await fs.writeFile(reportPath, `${reportLines.join('\n')}\n`)

  if (findings.length > 0) {
    console.error(`Found ${findings.length} potential XSS sink(s). See ${relativeReportPath}.`)
    process.exitCode = 1
    return
  }

  console.log(`No potential XSS sinks found. Report written to ${relativeReportPath}.`)
}

main().catch(error => {
  console.error(error)
  process.exitCode = 1
})